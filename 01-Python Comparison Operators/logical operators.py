# These cover the things that let you combine comparison operators
# They include: and, or, not
1 < 2
2 < 3
# How to chain these together?
# One way
1 < 2 < 3
# Use the and keyword
1 < 2 and 2 < 3
1 < 2 & 2 < 3
# Use the or keywork
1 < 2 or 2 < 3
1 < 2 | 2 < 3
# We can also negate the bool with not
not 1 < 2
not(1 < 2)
